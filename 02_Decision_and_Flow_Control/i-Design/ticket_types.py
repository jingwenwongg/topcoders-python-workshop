type = input()

if type == 'E' or type == 'e':
    print ("Early Bird Ticket")
elif type == 'D' or type == 'd':
    print ("Discount Ticket")
elif type == 'V' or type == 'v':
    print ("VIP Ticket")
elif type == 'S' or type == 's':
    print ("Standard Ticket")
elif type == 'C' or type == 'c':
    print ("Child Ticket")